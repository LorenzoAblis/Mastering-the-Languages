first_number = int(input("First Number: "))
second_number = int(input("Second Number: "))
operator = input("Operation: ")

if operator == "+":
    print(first_number + second_number)
elif operator == "-":
    print(first_number - second_number)
elif operator == "*":
    print(first_number * second_number)
elif operator == "/":
    print(first_number / second_number)